package org.jlibrary.core.entities;

import java.io.Serializable;

/**
 * @author Martin
 *
 * Resumed info of a repository. Util when we want to show a list of repositories on client and don't want 
 * to download entire repository structure
 */
public class RepositoryInfo implements Serializable {

	static final long serialVersionUID = -625650706461724068L;
	
    private String id;
    private String name;

    /**
     * @return Returns the id.
     */
    public String getId() {
        return id;
    }
    /**
     * @param id The id to set.
     */
    public void setId(String id) {
        this.id = id;
    }
    /**
     * @return Returns the name.
     */
    public String getName() {
        return name;
    }
    /**
     * @param name The name to set.
     */
    public void setName(String name) {
        this.name = name;
    }
    
    /* (non-Javadoc)
     * @see java.lang.Object#toString()
     */
    public String toString() {
        
        return name;
    }
}
